package com.bookstore.pojo;
public class Books {
    String bookname, bookpricture, bookmore;
    int bookid, bookmun;
    double bookprice;

    public String getBookname() {
        return bookname;
    }

    public void setBookname(String bookname) {
        this.bookname = bookname;
    }

    public String getBookpricture() {
        return bookpricture;
    }

    public void setBookpricture(String bookpricture) {
        this.bookpricture = bookpricture;
    }

    public String getBookmore() {
        return bookmore;
    }

    public void setBookmore(String bookmore) {
        this.bookmore = bookmore;
    }

    public int getBookid() {
        return bookid;
    }

    public void setBookid(int bookid) {
        this.bookid = bookid;
    }

    public int getBookmun() {
        return bookmun;
    }

    public void setBookmun(int bookmun) {
        this.bookmun = bookmun;
    }

    public double getBookprice() {
        return bookprice;
    }

    public void setBookprice(double bookprice) {
        this.bookprice = bookprice;
    }
}